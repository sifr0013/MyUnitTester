public class MyString
{
    String myString;

    public MyString()
    {
        myString = "";
    }

    public void setStringToBanana()
    {
        myString = "Banana";
    }

    public void setStringToApple()
    {
        myString = "Apple";
    }

    public void setStringToOrange()
    {
        myString = "Orange";
    }

    public void setStringToNull()
    {
        myString = null;
    }

    public String getString()
    {
        return myString;
    }
}
